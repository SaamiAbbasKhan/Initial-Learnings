interface EnumInterface {}
// This interface cause in ExpenseTracker section we'll do coding on interface concept when making
// enum arraylist to check for the category.

enum Market implements EnumInterface{
    GROCERIES, SHOPPING, CLOTHES, MARKET
}

enum Education implements EnumInterface{
    BOOKS, PEN, PENCIL, GEOMETRY, STATIONARY
}

enum Essentials implements EnumInterface{
    RENT, ELECTRICITY, BILLS, RECHARGES
}

enum Requirements implements EnumInterface{
    MEDICINES, FOOD, WATER, UTILITIES, MAINTENANCE, FEES,
}

enum Extras implements EnumInterface{
    TRAVEL, MOVIES, PARTIES, GIFTS
}

